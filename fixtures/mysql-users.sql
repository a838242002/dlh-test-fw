-- Initial schema + seed data for the mysql-pod-delete chaos scenario.
-- Loaded into MySQL by the fixture-minio-load-mysql WT step before chaos begins.
CREATE TABLE IF NOT EXISTS dlh_load (
  id   BIGINT AUTO_INCREMENT PRIMARY KEY,
  ts   DATETIME NOT NULL
) ENGINE=InnoDB;

-- 500 seed rows so SELECT queries return real results from the start.
INSERT INTO dlh_load(ts)
SELECT DATE_SUB(NOW(), INTERVAL seq SECOND)
FROM (
  SELECT (a.N + b.N*10 + c.N*100) AS seq
  FROM
    (SELECT 0 AS N UNION ALL SELECT 1 UNION ALL SELECT 2 UNION ALL SELECT 3 UNION ALL SELECT 4
     UNION ALL SELECT 5 UNION ALL SELECT 6 UNION ALL SELECT 7 UNION ALL SELECT 8 UNION ALL SELECT 9) a,
    (SELECT 0 AS N UNION ALL SELECT 1 UNION ALL SELECT 2 UNION ALL SELECT 3 UNION ALL SELECT 4
     UNION ALL SELECT 5 UNION ALL SELECT 6 UNION ALL SELECT 7 UNION ALL SELECT 8 UNION ALL SELECT 9) b,
    (SELECT 0 AS N UNION ALL SELECT 1 UNION ALL SELECT 2 UNION ALL SELECT 3 UNION ALL SELECT 4) c
) nums
WHERE seq < 500;
